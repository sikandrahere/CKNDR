import mongoose from "mongoose";

const productSchema = new mongoose.Schema({
    image:{
        type: String,
        required: true
    },
    name: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    size: {
        type: String,
        required: true
    },
    gender: {
        type: String,
        required: true
    },
    type: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    salePrice: {
        type: Number,
        required: true
    },
    totalStock: {
        type: Number,
        required: true
    }
    
})

const Product = mongoose.model("Product", productSchema);

export default Product